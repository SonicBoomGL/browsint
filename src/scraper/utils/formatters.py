    report_parts.append(f"{HEADER_BLUE}‚ïë {TEXT_WHITE}{'üåê ANALISI LOCALE PAGINA WEB':^{HEADER_WIDTH-4}}{HEADER_BLUE} ‚ïë{Style.RESET_ALL}")
